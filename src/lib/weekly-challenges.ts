/**
 * يرجع التحدي الأسبوعي بناءً على عدد الأسابيع بعد إتمام الرحلة
 */
export function getWeeklyChallenge(weeksSinceCompletion: number): WeeklyChallenge | null {
  if (weeksSinceCompletion < 1) return null;
  // Weeks 1-4: specific challenges. After that: cycle through them
  const index = ((weeksSinceCompletion - 1) % WEEKLY_CHALLENGES.length);
  return WEEKLY_CHALLENGES[index] ?? null;
}

/**
 * يرجع رقم اليوم داخل الأسبوع (1-7)
 */
export function getDayInWeek(): number {
  const day = new Date().getDay(); // 0=Sun, 6=Sat
  // Start week from Saturday (Arabic week)
  return day === 6 ? 1 : day + 2;
}
