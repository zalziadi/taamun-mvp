/**
 * Returns today's verse based on day-of-year, cycling through the 30 verses
 */
export function getTodayVerse(): DailyVerse {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((now.getTime() - start.getTime()) / (1000 * 60 * 60 * 24));
  const index = dayOfYear % POST28_VERSES.length;
  return POST28_VERSES[index];
}
